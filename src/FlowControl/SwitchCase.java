package FlowControl;

public class SwitchCase {

    //Practicing switch-case
    public static void main(String[] args) {
        char ch = 'A';

        switch (ch){
            case 'A':
                System.out.println("This is first character of English alphabet.");
                break;

            default:
                System.out.println("This is a default case in switch.");
        }
    }
}
